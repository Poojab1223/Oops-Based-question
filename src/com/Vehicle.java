package com;

public class Vehicle {
	String id;
	String vehicleType;
	
	public Vehicle(String id) {
		super();
		this.id = id;
		
	}
	
	public String getVehicleType() {
		return vehicleType;
	}
	public void setVehicleType(String vehicleType) {
		this.vehicleType = vehicleType;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	

}
